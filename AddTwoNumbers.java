public class AddTwoNumbers {
    public static void main(String[] args) {

        // 1. Declare two variables
        int number1 = 10;
        int number2 = 20;

        // 2. Add the two numbers
        int sum = number1 + number2;

        // 3. Print the result
        System.out.println("The sum is: " + sum);
    }
}